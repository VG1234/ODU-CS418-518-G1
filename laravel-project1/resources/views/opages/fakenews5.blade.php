                <div class="content">It depends on your definition of "work".  It will fill the crack and make it look better temporarily, but so will spraying with with WD-40, filling it with transmission fluid, etc.  These are all old tricks used by shady used car dealers and auction yards for many years.  These home remedies are not going to stop a crack from spreading and they are not going to improve the appearance of the crack long term.  Most importantly they contaminate the crack making it difficult for a professional to complete a quality repair.  <br>
    <br>
    To me it's like putting sawdust in the crankcase to quiet lifters and keep the engine from smoking.  It will make a difference temporarily, but in the long run does more harm than good.</div>
    
                
                
                                        
