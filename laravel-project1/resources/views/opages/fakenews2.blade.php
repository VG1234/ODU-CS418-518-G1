<h1 class="entry-title" style="text-align: center"><b>Twitter is relaunching the reverse-chronological feed as an option for all users starting today</b></h1>



<div class="td-module-meta-info">
<div class="td-post-author-name"><div class="td-author-by">By</div> <a href="">Casey Newton</a><div class="td-author-line"> ---------- </div> </div>                    <span class="td-post-date"><time class="entry-date updated td-module-date" datetime="2020-11-27T09:56:03+00:00">27th November 2020</time></span>                                                        </div>

</header><br>

</div>

<div class="td-post-sharing-top"></div>

<div class="td-post-content">

<div class="td-post-featured-image"><a href="https://dogstodaymagazine.co.uk/wp-content/uploads/2020/11/Roxi-parcel-dog-copyright-Vets-Now-1-scaled-e1606470770170.jpg" data-caption="" class="td-modal-image"></a></div>
<p>Twitter is offering users another escape hatch from its ranked timeline. The company said today that it will introduce a prominent new toggle in the app to switch from the ranked timeline to the original, reverse-chronological feed. The company says the move comes in recognition of the fact that Twitter is often most useful in real time, particularly during live events such as sports games or the Oscars.</p>
<p>Twitter began ranking the timeline almost four years ago. It was an effort to increase usage at a time when Facebook had pulled dramatically ahead of Twitter, raising doubts about the company’s future and setting it on a course to reinvent itself. Many users griped about the change, even though Twitter has always allowed users to switch back to the reverse-chronological feed temporarily.</p>
<p>The latest incarnation of the original Twitter feed can be accessed by tapping the cluster of small stars — the company calls it the “sparkle” and now so shall we all, forever — and switching to see the latest tweets. Over time, the company will learn your behavior. If you routinely switch to the latest tweets, Twitter will default you to them. This marks a change from the past, when the app would switch you back to the ranked timeline at unpredictable intervals.</p>
<p>Keith Coleman, vice president of product at Twitter, told The Verge that in tests, users who had access to the easy toggle participated in more conversations than average. I’ve been one of those users, and the toggle has been a welcome change. The flagship Twitter app’s strange aversion to real-time tweets — the lifeblood of the service since forever — is the main reason I continue to use Tweetbot on every platform, despite how it gets worse every year due to API restrictions.</p>
<p>Twitter’s re-embrace of live tweets gives me hope that the company will continue to elevate real-time features across the platform. The company’s support for its pro app, Tweetdeck, is halfhearted at best. Investing more in features for power users will help ensure Twitter retains its place as the beating heart of breaking news around the world — and ignoring them creates opportunities for competitors.</p>
<p>The toggle will begin rolling out today on iOS and will come to other platforms in the coming weeks, the company said.</p>

