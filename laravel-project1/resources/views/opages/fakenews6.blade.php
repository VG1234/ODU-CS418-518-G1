<p>Users replicated the chain ‘I do not authorize’ through which, supposedly, they did not give permission to WhatsApp, Facebook or Instagram to use “images, information, messages, photos, deleted messages, files, etc.”
</p>
<p>This is due to the fact that the messaging application WhatsApp announced changes to its terms of service and privacy policy at the beginning of this month.</p>
<p>The information is false. Similar content circulated in Facebook and Instagram posts since 2012, as we verified in Verificador. Now, WhatsApp has been added to this hoax, which since 2014 is part of the Facebook companies.</p>
<p><b>What do the new WhatsApp policies say?</b>
    The information that WhatsApp collects –that has not been added in the last update of terms and conditions, but dates from before– includes, as they themselves state on their page, the following data:
    However, there are new points that appear in the updated version of the privacy policy and data exchange of WhatsApp with Facebook. The Protected Data Organization highlights the following:
    This new data access authorization includes companies that use WhatsApp Business. This will allow them to chat with their customers and that service provider companies can access interactions between users and other companies.
    The previous point allows the optimization of WhatsApp Business, which can use the Facebook infrastructure for storage services
    If a business uses this function, the user will be informed about it
    Facebook will not automatically use the business messages that are given through WhatsApp nor will it be for its own marketing purposes
    The above points have no impact on messages or private calls made with groups of friends and family
    The restriction of access to third parties through WhatsApp end-to-end encryption is maintained. So that the content of the messages (including the photos you send) cannot be shared even with Facebook</p>
