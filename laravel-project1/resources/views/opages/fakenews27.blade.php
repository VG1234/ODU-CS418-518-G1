<h3>NASA'S asteroid tracking systems have detected a space rock on "close approach" this weekend and the asteroid is headed our way at nearly 20,000mph (32,186km/h).</h3>
<p>The speedy <a data-link-tracking="InArticle|Link" href="/latest/asteroids" rel="tag">asteroid</a>, dubbed 2020 BL14, is comparable in height to the Leaning Tower of Pisa, Italy, and about six London double-decker buses lined up in a row. <a data-link-tracking="InArticle|Link" href="/latest/NASA" rel="tag">NASA</a> said the <a data-link-tracking="InArticle|Link" href="/latest/Space" rel="tag">space</a> rock will approach us on the afternoon of Sunday, February 16.</p>
<p>The news comes just two weeks after NASA confirmed BL14â€™s presence in the solar system.</p>
<p>Spotted on January 29, this year, the asteroid is trapped on an orbit that will bring it close to Earth around 3.29pm GMT (10.29am EST) on Sunday.</p>
<p>NASA classifies NEOs as all comets and asteroids that orbit the Sun from a distance smaller than or equal to 1.3 astronomical units (au).</p>

