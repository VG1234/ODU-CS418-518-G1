<p>!!!!!All this information is gathered from twitter!!!!!</p>
<p>No, 'how to hit a woman so no one knows' wasn't typed into Google '163 million times'.</p>
<p>That's the number of *search results* Google returns when you search for these words.</p>
<p>Violence against women/girls is an important issue but this incredibly sloppy 'research' helps no-one.  </p>
<p>And this is easy to check, too. You can just load up Google trends and see for yourself. There is no data for "I am going to kill her when she gets home", or "How to hit a woman so nobody knows"; essentially nobody has been searching for these terms.</p>
<p>For comparison, this is how an extremely popular search term "BTS" and an extremely niche, specific search term "Pioneer CDJ200" are shown on Google trends. Note that even incredibly low levels of search interest are still shown.</p>
<p>No, she isn't. The paper she's referring to is using search results and confusing that number with number of searches, and the tweeter has made the same mistake.</p>
