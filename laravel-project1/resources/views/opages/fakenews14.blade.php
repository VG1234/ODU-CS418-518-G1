<h1 class="entry-title" style="text-align: center"><b>False claim: Video shows 5G telecoms equipment stamped with ‘COV-19’ ready to be installed into a mast</b></h1>



<div class="td-module-meta-info">
<div class="td-post-author-name"><div class="td-author-by">By</div> <a href="">Reuters Staff</a><div class="td-author-line"> ---------- </div> </div>                   
<span class="td-post-date"><time class="entry-date updated td-module-date" datetime="2020-11-27T09:56:03+00:00">
    15th May 2020</time></span>                                                        </div>

</header><br>

</div>

<div class="td-post-sharing-top"></div>

<div class="td-post-content">

<div class="td-post-featured-image"><a href="https://dogstodaymagazine.co.uk/wp-content/uploads/2020/11/Roxi-parcel-dog-copyright-Vets-Now-1-scaled-e1606470770170.jpg" data-caption="" class="td-modal-image"></a></div>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">A video being shared widely on social media shows a man who presents himself as a telecoms engineer allegedly revealing equipment labelled COV-19 that is due to be installed into a 5G tower. This video contains false information.</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">The footage emerged earlier this week and has been shared on multiple social networks, garnering thousands of views (<a href="https://www.facebook.com/emma.vella80/videos/3029161027164374">here</a>, <a href="https://www.facebook.com/stuart.mckenzie.3386/videos/1336880243176546">here</a>, <a href="https://www.instagram.com/p/CAK2uuXnAvU/">here</a>).&nbsp;</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">It shows a man wearing a hard hat and high-visibility jacket telling the camera that he works as a telecoms engineer erecting 5G masts across the UK while the country is in lockdown. He then holds up a circuit board and claims it is “a piece of kit that has COV-19 on it”.</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">He adds: “I don’t really know any product by any company that produces circuitry like this that has the brand name COV-19, but that’s what it says on the fucking circuit board. I’m not a fucking conspiracy theorist but obviously I’ve read all that stuff online about coronavirus and COVID-19, but why the fuck are they putting circuitry like that in towers like that?”</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">It is not clear whether the subject in the video is actually a telecoms engineer, but the equipment he is holding is not 5G telecoms kit. The circuit board was actually taken from a Virgin Media box for cable television - the casing of which can be seen on the bonnet of the van as the camera pans at the end of the footage.</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">Virgin Media confirmed to Reuters it believed the circuit board had been taken from an old TV box, and the “COV-19” engraving was not authentic.</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">A company spokesman added: “That is a board from a very old set top TV box and which never featured any component parts inscribed/stamped/printed or otherwise with COV 19. It has absolutely no relation with any mobile network infrastructure, including that used for 5G.”</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">A grey cable connector on the far corner of the circuit board in the video is situated next to the SCART plug, as seen on an image of a Virgin Media TV box (<a href="https://store.virginmedia.com/content/dam/eSales/TV/responsive/tv-boxes/tv-tv-boxes-vhd-box-technical-stuff-a1.jpg">here</a>) .&nbsp;</p>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">Reuters was able to compare the buttons seen on the near-sided edge of the board with pictures sent by two sources who owned the same box and had pulled it apart. The layout of the circuit board also matched the pictures sent by the sources.</p>
<h2 class="Headline-headline-2FXIq Headline-black-OogpV ArticleBody-heading-3h695">VERDICT</h2>
<p class="Paragraph-paragraph-2Bgue ArticleBody-para-TD_9x">False. The video does not show COV-19 labelled equipment ready to be fitted to a 5G mast.</p>
