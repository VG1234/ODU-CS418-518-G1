<p>A video of a classical dancer that is said to be from Shanghai Disney Resort has gone viral on social media and is being widely shared on WhatsApp as well.</p>
<p>The caption shared along with the video claims that the classical dancer is not a living person but rather a robot created by Shanghai Disney.</p>
<p><em>“The classical dancer in this video is not a real living person but a newly constructed robot! The video, a product of Shanghai Disney, is only a five-minute show but it would take more than four hours queuing up to purchase a ticket which costs 499 yuan per person. The robot is entirely Chinese-made and its technical level has surpassed those of Japan. This video is indeed worthy of watching (sic),”</em> reads the caption.</p>
