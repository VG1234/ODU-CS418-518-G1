<p>Facebook is updating the list of words that, when included in a status update, trigger hidden animations.</p>
<p>The social network has been experimenting with these for awhile — you've probably seen them on posts with the words "congratulations" or "xoxo" (or some variation thereof.) When posted, these words appear as a different color and clicking on them triggers a brief animation (balloons or hearts, for example.) </p>
<p>Now, Facebook is updating the list of words that triggers these animations. Additions include: "rad," "bff," "lmao," and "thank you so much." All of those words will now come with their own colors and animation effects.</p>
<p>Facebook is also updating the animations associated with reactions to "life events," like engagements and weddings. Now, when people add a reaction to a "life event," the reaction will come alongside animated reactions that fill the update. </p>
