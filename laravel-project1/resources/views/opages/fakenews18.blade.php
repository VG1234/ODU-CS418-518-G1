<p>The updates are the latest way Facebook is turning to colorful animations to spice up the News Feed. The social network is always looking for ways to boost "original sharing" — posts that are, you know, actual thoughts, not just reposting of memes and viral videos.</p>
<p>Adding more animations and features like its <a href="/article/facebook-color-statuses">colorful status</a> updates are one way to accomplish that goal. (It's also not a bad distraction from all that <a href="/article/facebook-russia-government-regulation">Russia stuff</a>.)</p>
<p>Either way, though, get ready to see a lot more random animated emoji on Facebook.</p>



