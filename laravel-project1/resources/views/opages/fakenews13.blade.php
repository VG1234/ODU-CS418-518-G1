<h1 class="entry-title" style="text-align: center"><b>Facebook is adding the option to charge for access to live streams</b></h1>



<div class="td-module-meta-info">
<div class="td-post-author-name"><div class="td-author-by">By</div> <a href="">Chaim Gartenberg</a><div class="td-author-line"> ---------- </div> </div>                   
<span class="td-post-date"><time class="entry-date updated td-module-date" datetime="2020-11-27T09:56:03+00:00">
    Apr 28, 2020</time></span>                                                        </div>

</header><br>

</div>

<div class="td-post-sharing-top"></div>

<div class="td-post-content">

<div class="td-post-featured-image"><a href="https://dogstodaymagazine.co.uk/wp-content/uploads/2020/11/Roxi-parcel-dog-copyright-Vets-Now-1-scaled-e1606470770170.jpg" data-caption="" class="td-modal-image"></a></div>
<p id="T3yNU5">As more businesses, creators, and artists turn to internet live-streaming tools during the pandemic, <a href="https://about.fb.com/news/2020/04/introducing-messenger-rooms/">Facebook has announced</a> that it’ll be adding the option for people to charge for access to events with Facebook Live streams. </p>
<p id="NINXxe">“To support creators and small businesses, we plan to add the ability for Pages to charge for access to events with Live videos on Facebook – anything from online performances to classes to professional conferences,” the announcement reads. </p>
<p id="f3EaIq">Details on the new feature are slim right now — the news came buried in Facebook’s larger announcement about its new Zoom-like <a href="https://www.theverge.com/2020/4/24/21233468/facebook-messenger-rooms-live-instagram-live-igtv-video-chat">Messenger Rooms feature</a> — and there’s no real date for when users will be able to charge for events outside of a vague promise that it’ll arrive in the “coming weeks.” The announcement references that pages will be able to charge for events, but Facebook has yet to clarify if there will be any limitations as to who’ll be able to use the feature.  </p>
<p id="o0ITCp">Additionally, Facebook is adding the option for event creators to mark their events as “online only,” which makes sense since no one can go to in-person events right now due to ongoing social distancing protocols around the world. </p>

