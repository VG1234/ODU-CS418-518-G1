<div class="td-post-featured-image"><a href="https://dogstodaymagazine.co.uk/wp-content/uploads/2020/11/Roxi-parcel-dog-copyright-Vets-Now-1-scaled-e1606470770170.jpg" data-caption="" class="td-modal-image"></a></div>
<p> An earlier version of this story incorrectly stated that XRVision facial recognition software identified Antifa members among rioters who stormed the Capitol Wednesday. XRVision did not identify any Antifa members. The Washington Times apologizes to XRVision for the error.</p>
<p>Facial recognition software has identified neo-Nazis and other extremists as participants in Wednesday’s assault on the U.S. Capitol.</p>
<p>XRVision, a software firm in New York, used its image analysis to help law enforcement identify rioters.

    “Shortly after the rioting started, XRVision performed an analysis on the footage and identified several individuals. This information was shared with LEA [law enforcement agency],” the company said in a statement to The Washington Times.</p>
    <p>Supporters of President Trump, including some who were part of the crowd Wednesday that stormed the U.S. Capitol, had claimed online that Antifa members had infiltrated their numbers.</p>
    <p>The Washington Times erroneously reported late Wednesday that facial recognition technology backed up that speculation and identified two Antifa members. In fact, XRVision has not identified any members of that far-left movement as being part of the attack.</p>
<p>There is other evidence Antifa members may have been there.

    The New York Post, quoting a law enforcement source, said two Antifa from New York were in the crowd.
    
    A video on social media also seems to show people wearing Trump “Make America Great Again” paraphernalia shouting “Antifa, Antifa” as a man with some type of instrument tried to smash a window and break into the Capitol. MAGA people then forcibly subdued the man to prevent further damage.</p>
    <p>A law enforcement source gave this day-after account to The Washington Times.

        “The professional protesters were in the crowd posing as Trumpers. They were preaching violence. As they approached it was announced that [Vice President Mike] Pence had said he has no Constitutional authority. The crowd got mad. The agitators used this to whip-up anger. If the Feds are really intent on making the linkage between the instigators and Antifa, the evidence is there.”</p>
<p>The presence of Antifa became part of Wednesday’s divisive debate over counting the electoral votes to confirm President-elect Joseph R. Biden.</p>
<p>Several Republican lawmakers said the assault struck them as an Antifa operation.

    “This has all the hallmarks of Antifa provocation,” Rep. Paul Gosar, Arizona Republican, wrote on Twitter.
    
    Democrats rejected those claims as baseless.</p>
