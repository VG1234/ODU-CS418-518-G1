<p>Floridians may have a tougher time finding a medical specialist over the next 10 years, a new study suggests.</p>
<p>The state faces a critical shortage of specially trained doctors — from psychiatrists to general surgeons — unless more medical training programs are created, the state's top teaching hospitals warned Tuesday in what it called a landmark study.</p>
<p>The shortage will grow to 7,000 specialist physicians throughout Florida by 2025, according to a study commissioned by the Teaching Hospital Council of Florida and the Safety Net Hospital Alliance of Florida, an association of 14 teaching, public and children's hospitals providing specialized medical care and training for future doctors.</p>
<p>The shortage spans 19 specialties, with the largest areas of need in psychiatry, general surgery, rheumatology and thoracic surgery, according to the study.

    Broward County faces the most acute shortage for general surgeons, thoracic surgeons and ear, throat and nose specialists, the analysis found.</p>
<p>By 2025, the county will need 77 more general surgeons, 14 more thoracic surgeons and 13 more otolaryngologists.

    Palm Beach County will see the worst shortage in general surgeons (101 short of needs), family practitioners (312), kidney disease experts (16) and specialists in hematology and oncology (55) as well as allergy, immunology and infectious diseases (41), the study found.</p>
<p>The two counties have an aging population and also will need more internists and gynecologists to handle an increasing caseload, said Dr. Nabil El Sanadi, president and CEO of Broward Heath, who read the report.</p>
<p>We need to ramp up our training and recruitment of good health care providers," said El Sanadi, who leads one of the state's seven teaching hospitals.

    Florida ranks near the bottom of states in the ratio of medical residencies to population. To catch up, Florida needs to create about 1,360 residency slots a year for the next 10 years, according to the study, which looked at the state's current number of doctors, its growing population and the residency programs now offered in Florida.</p>
<p>The global information company IHS conducted the analysis using computer simulation models to study the shortage of specialist doctors in Florida based on the current number of doctors, growing population and residency programs now offered.

    The state has recently opened new medical schools at several state universities including Florida International, Florida Atlantic and Florida State. But residencies to train their graduates haven't kept up.</p>
<p>"We are seeing now that the state of Florida has fallen behind in training an adequate number of physicians to meet our residents' needs for quality health care,'' El Sanadi said.</p>
