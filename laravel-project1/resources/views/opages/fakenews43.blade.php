<p><span class="detailImageDesc"><b>Above</b>: Mars in the morning sky on Thursday, June 19th, as seen from mid-northern latitudes. If you live in the southern hemisphere, <a href="/images/content/45052main_skymap_south.gif" title="">click here</a>.</span></p>

<p>Mars is a morning planet now. You have to wake up early to see it. Soon, though, it will be more conveniently placed. By mid-July Mars will rise in the east around 11 p.m. local time. In late August it will appear as soon as the sun sets. It won't be long before everyone can see Mars at a civilized hour.</p>

<p>We'll be telling more stories about Mars in the weeks ahead. This one, though, is finished. Did you make it to the end? Congratulations! You're now 2000 km closer to Mars.</p>
