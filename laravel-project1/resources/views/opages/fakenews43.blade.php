<p><a href="javascript:openNASAWindow('/images/content/45053main_Wayman1.jpg')"><img width="250" alt="Mars seen through the eye piece of an 8-inch telescope on June 11, 2003" title="Mars seen through the eye piece of an 8-inch telescope on June 11, 2003" src="/images/content/45054main_Wayman1_med.jpg" height="208" align="RIGHT" border="1"></a><b>June 18, 2003</b>: Count slowly: <i>one one-thousand, two one-thousand, three one-thousand....</i> You just got about 30 km closer to the planet Mars.</p>

<p>Earth and Mars are rapidly converging. On August 27, 2003 -- the date of closest approach -- the two worlds will be 56 million km apart. That's a long way by Earth standards, but only a short distance on the scale of the solar system. NASA, the European Space Agency and Japan are all sending spacecraft to Mars this year. It's a good time to go.</p>

<p><span class="detailImageDesc"><b>Right</b>: This is what Mars looked like through the eye piece of an 8" telescope on June 11th. Image credit: Ron Wayman of Tampa, FL.</span></p>

<p>Between now and August, Mars will brighten until it "blazes forth against the dark background of space with a splendor that outshines Sirius and rivals the giant Jupiter himself." Astronomer Percival Lowell, who famously mapped the canals of Mars, wrote those words to describe the planet during a similar close encounter in the 19th century.</p>

<p>Already Mars is eye-catching. You can see it this month in the morning sky -- bright, steady and remarkably red. Only Venus near the sun is brighter.</p>
             
<p>Amateur astronomers looking through backyard telescopes have reported in recent days great views of Mars's south polar cap. Made of frozen water and carbon dioxide ("dry ice"), it reflects sunlight well. "I can see the polar ice vividly using my 8-inch telescope," says Ron Wayman of Tampa, Florida. He's also spotted "some faint darker-shaded areas on the surface."</p>

<p>Such markings will become clearer in the weeks ahead. On June 1 Mars was 12.5 arcseconds across and it glowed like a -1st magnitude star. On August 27th it will be twice as wide (25 arcseconds) and six times brighter (magnitude -2.9).</p>

<p><a href="javascript:openNASAWindow('/qt/45056main_ReddyMovie_big.qt')"><img width="250" alt="Animation showing how Mars will swell in size between June and August 2003" title="Animation showing how Mars will swell in size between June and August 2003" src="/images/content/45050main_ReddyAnim_med.gif" height="227" align="LEFT" border="1"></a><span class="detailImageDesc"><b>Left</b>: Frank Reddy, author of <a href="javascript:openNASAWindow('http://www.spaceweather.com/delights/')">Celestial Delights</a>, created this animation showing how Mars will swell in size between June and August 2003. Click on the image to view the <a href="/qt/45056main_ReddyMovie_big.qt">complete movie</a> spanning all of 2003.</span></p>

<p>Much has been made of the fact that the August 27th encounter with Mars is the closest in some 60,000 years. Neanderthals were the last to observe Mars so favorably placed. This is true. It's also a bit of hype. Mars and Earth have been almost this close many times in recent history.</p>

<p>Some examples: Aug. 23, 1924; Aug. 18, 1845; Aug. 13, 1766. In each case Mars and Earth were approximately 56 million km apart.</p>

<p>Astronomers call these close encounters "perihelic oppositions." <i>Perihelic</i> means Mars is near perihelion -- its closest approach to the sun. (The orbit of Mars, like that of all planets, is an ellipse, so the distance between the sun and Mars varies.) <i>Opposition</i> means that the sun, Earth and Mars are in a straight line with Earth in the middle. Mars and the sun are on <i>opposite</i> sides of the sky. When Mars is at opposition and at perihelion -- at the same time -- it is very close to Earth.</p>

<p>August 27 is indeed the best perihelic opposition since the days of the Neanderthals, but it scarcely differs from other more recent ones. That's fine because all perihelic oppositions of Mars are spectacular.</p>

