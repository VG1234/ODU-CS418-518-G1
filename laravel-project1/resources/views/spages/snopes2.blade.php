			<p>On June 26, some Twitter users noticed that they were unable to switch from the default timeline mode based on algorithms to the chronological timeline mode when using a web browser application. A few days later, tech website 9to5mac wrote an article stating that chronological tweets “seemingly won’t last”:</p>
	
	<p>An added bit of support for this thesis came from out-of-date or conflicting information from Twitter, as reported by The Verge:</p>
	
	<p>There are, however, no plans to remove the option to view tweets chronologically, according to Twitter. Responding to the 9to5mac article, Twitter product lead Kayvon Beykpour tweeted that the issue was a bug and that they were “working on fixing it.” Later, Twitter announced that the problem had been fixed:</p>
	
	<p>Because the issue was caused by a bug that has since been corrected, we rate the claim that the option to view tweets chronologically is disappearing as “False.”</p>
	
