			<h3 class="card-header h6 border border-bottom-0" style="font-weight: 700">Origin</h3>
	
	
	
	
		<div class="single-body card card-body rich-text">
	
			<p>A viral bit of copypasta shared repeatedly on WhatsApp has alleged two things: That a new policy will allow WhatsApp to “use” or “publish” your photos and private posts, and that if you do not want to give WhatsApp this permission, you can simply post a disclaimer barring the platform from doing so.</p>
	
	<p>First of all, as an end-to-end encryption messaging service, WhatsApp does not have the ability to view or access private messages or photos. While WhatsApp released a change in their privacy policy that required business accounts to share some data with Facebook in January 2021, this policy was incorrectly interpreted to apply to private accounts, and its implementation has been delayed until May 2021. </p>
	
	<p>Second of all, the notion that a private user can change the existing terms of their user agreement with an internet company by simply posting a putatively legal sounding post is one of the oldest tropes of the social media age. It first appeared on Facebook in the late 2000s, and later Instagram, Snapchat, and others. As we have previously reported in those cases, nothing you post on a social media platform alters the terms of use agreement you signed by joining that platform.</p>
	
	<p>Because WhatsApp is not implementing policy changes allowing them to use or publish your private posts, and because a legal-sounding disclaimer does nothing to alter existing terms of service, this claim is “False.”</p>
	
