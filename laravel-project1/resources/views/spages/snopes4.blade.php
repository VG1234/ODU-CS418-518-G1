			<h3 class="card-header h6 border border-bottom-0" style="font-weight: 700">Origin</h3>
	
	
	
	
		<div class="single-body card card-body rich-text">
	
			<p>In late April 2021, social media posts, like the one displayed below, Google users allegedly asked the search engine “how to a hit a woman so now one knows” more often during the first phase of lockdowns to prevent the spread of COVID-19 in 2020 than during the same time frame the previous year.</p>
	
	<p>The claim originated from media sources including Blavity (a Los Angeles-based platform serving Black millennials), UNILAD (a British media outlet), and MSNBC (an American cable network owned by NBC Universal)</p>
	
	<p>We obtained an archived version of an April 26 opinion column by the latter outlet, a piece of commentary that cited peer-reviewed research analyzing Google searches from the beginning of the U.S. COVID-19 outbreak in March 2020 to the end of August that year. </p>
	
	<p>In other words, Google users purportedly searched “how to hit a woman so no one knows” 163 million times between March and August 2020, and that number was 31 percent higher than the amount of same searches during those months the previous year.</p>
	<p>However, that was not true. One day after the MSNBC column published, onlookers identified flaws in the research’s methodology, and its author confirmed the findings were inaccurate. As a result, the news outlet removed the above-transcribed section from its article citing the research. (That updated version of the story remained live online as of this writing.)</p>
	<p>We looked at Google search data ourselves. By searching the phrase “how to hit a woman so no one knows,” the platform produces billions of returns — or websites or posts that contain those keywords. That is not the same as the number of times users searched the question (we explain how to find that quantity below), and it appeared to grow significantly after the MSNBC article and other media coverage.</p>
	<p>By using the separate Google Trends search engine — a tool that compiles data for Google search queries worldwide — we learned that relatively no one used the website to look for information on “how to hit a woman so no one knows,” including during the time window referenced in the study (March to August 2020)</p>
	<p>Next, to fully explore the issue, we reached out to Standish (the author of the study called, “COVID-19, suicide, and femicide: Rapid Research using Google search phrase“) to learn about her research process and its shortcomings.
	
	She responded to us via email, saying she indeed mistook the amount of search returns for the number of times Google users entered the phrase looking for answers. After the revelation, she said she alerted The Journal of General Psychology of the error and requested an assessment of the entire study.</p>
	<p>he results are meaningless … all I have done is demonstrate a rise in web pages over the year. Not at all what I thought I was doing,” Standish said. “[The] goal of the study was to look for people who were hurting to elevate what they are going through and to help.</p>
	
	<p>“The first time I googled these terms my heart skipped…I was shocked,” she continued in a tweet thread. “I sincerely apologise and will make amends.”
	
	In sum, we rate this claim “false” — millions of people, presumably men, did not turn to Google for help to seemingly conceal physical abuse against their female partners during the early months of the pandemic.</p>
